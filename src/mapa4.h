void mover_personaje_wasd() {
int px = -1, py = -1;
for (int i = 0; i < FIL; ++i) {
    for (int j = 0; j < COL; ++j) {
        if (mapa[i][j] == 2) {
            px = i;
            py = j;
            break;
        }
    }
    if (px != -1) break;
}
    imprimir_mapa4();
    while (true) {
        char tecla = _getch();
        int nx = px, ny = py;
        if (tecla == 'w') nx--;
        else if (tecla == 's') nx++;
        else if (tecla == 'a') ny--;
        else if (tecla == 'd') ny++;
        else if (tecla == 'q') break; // salir con q
        if (nx >= 0 && nx < FIL && ny >= 0 && ny < COL && mapa[nx][ny] == 0) {
            mapa[px][py] = 0;
            px = nx; py = ny;
            mapa[px][py] = 2;
        }
        system("cls");
        imprimir_mapa4();
    }
}


