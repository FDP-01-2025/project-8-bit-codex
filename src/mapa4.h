void inicializar_mapa4() {
    for (int i = 0; i < FIL; ++i) {
        for (int j = 0; j < COL; ++j) {
            if (i == 0 || i == FIL - 1 || j == 0 || j == COL - 1) {
                mapa[i][j] = 1; 
            } else {
                mapa[i][j] = 0; 
            }
        }
    }
}

void imprimir_mapa4() {
    int espacios = (ANCHO_TERMINAL - COL) / 2;
    for (int i = 0; i < FIL; ++i) {
        for (int s = 0; s < espacios; ++s) cout << " ";
        for (int j = 0; j < COL; ++j) {
            if (mapa[i][j] == 1)
                cout << "#";
            else if (mapa[i][j] == 2)
                cout << "*";
            else if (mapa[i][j] == 3)
                cout << "@";
            else
                cout << " ";
        }
        cout << endl;
    }
}

