void mover_personaje_wasd(character &player) {
int px = -1, py = -1;
for (int i = 0; i < FIL; ++i) {
    for (int j = 0; j < COL; ++j) {
        if (mapa[i][j] == 2) {
            px = i;
            py = j;
            break;
            
        }
    }
    if (px != -1) break;
}
    imprimir_mapa3();
    while (true) {
        char tecla = _getch();
        int nx = px, ny = py;
       switch (tecla)
        {
        case 'w':
        case 'W':
        case TECLA_ARRIBA:
        nx--;
        break;
        case 's':
        case 'S':
        case TECLA_ABAJO:
        nx++;
        break;
        case 'a':
        case 'A':
        case TECLA_IZQUIERDA:
        ny--;
        break;
        case 'd':
        case 'D':
        case TECLA_DERECHA:
        ny++;
        break;
        case 'q':
        case 'Q':
        case ESC:
        break;
        }
        if (nx >= 0 && nx < FIL && ny >= 0 && ny < COL) {
    int celda = mapa[nx][ny];

    if (celda == 0) {
        // Movimiento libre
        mapa[px][py] = 0;
        px = nx; py = ny;
        mapa[px][py] = characters;

    } else if (celda == zombies) {
        cout << "\n¡Te encontraste con un ZOMBI! \n";
        sleep(1); 
        combatSystem(player, zombie);
        mapa[nx][ny] = 0; // Elimina el zombi
        mapa[px][py] = 0;
        px = nx; py = ny;
        mapa[px][py] = characters; 

    } else if (celda == eskeletons) {
        cout << "\n¡Un ESQUELETO bloquea tu camino! \n";
        sleep(1);
        combatSystem(player, skeleton);
        mapa[nx][ny] = 0; // Elimina el esqueleto
        mapa[px][py] = 0;
        px = nx; py = ny;
        mapa[px][py] = characters;

    } else if (celda == chests) {
        cout << "\n¡Has encontrado un COFRE! \n";
        string objeto = chest();
            if (objeto != "Enemy Encountered" && objeto != "Empty Chest" && !objeto.empty())
            {
                addToInventory(objeto);
            }
        sleep(2);
        mapa[nx][ny] = 0; // Quita el cofre
        mapa[px][py] = 0;
        px = nx; py = ny;
        mapa[px][py] = characters;

    } else if (celda == boss) {
        cout << "\n¡Te enfrentas al JEFE FINAL! \n";
        combatSystem(player, Dark_knight);
        sleep(2);
        
    }
}

        Sleep(0);
        system ("cls");
        imprimir_mapa3();
    }
}


